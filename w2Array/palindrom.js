"use strict";
/**
 * 
 * @param {array} arr inputs the array
 * @return {array} returns new array
 */
function isPalindrome(arr) {
    let newArray = [];

    for (let i = 0; i < arr.length; i++) {
        if (arr[i] === arr[arr.length - 1]) {
            newArray.push;
        }
    }
    return newArray;
}
let array1 = [1, 2, 3, 4];
console.log(isPalindrome(array1));